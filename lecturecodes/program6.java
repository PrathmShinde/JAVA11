// java Wrapper class
// java SIZEof operator ( in java, we cannot use sizeof Operator/keyword )
// Instead of sizeoff we can use below codeLines.

class SizeOf {

	public static void main(String[] args) {

		System.out.println(Integer.SIZE/8);	//4
		System.out.println(Character.SIZE/8);	//2
		System.out.println(Float.SIZE/8);	//4
		System.out.println(Double.SIZE/8);	//8			
	}
}
